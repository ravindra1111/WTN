package com.wipro.patterns;

public class Ex_5_patterns5 {

	public static void main(String args[])
	{
		int i,j;
		for(i=1;i<=3;i++)
		{
			for(j=1;j<=i+1;j++)
				System.out.print(j+" ");
			System.out.println("");
		}
		
		/*
		 this program prints the pattern
		 1 2
		 1 2 3
		 1 2 3 4

		 */
	}
	
}
